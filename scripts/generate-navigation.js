// scripts/generate-navigation.js
import fs from 'fs';
import path from 'path';
import chokidar from 'chokidar';
import { fileURLToPath } from 'url';

// --- Configuration ---
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const dataFolder = process.env.DATA_FOLDER || 'quintessential-essence';
console.log(`[GEN-NAV] 📂 Đang sử dụng nguồn dữ liệu: public/${dataFolder}`);

const markdownDir = path.join(__dirname, '..', 'public', dataFolder);

const outputFile = path.join(__dirname, 'navigationData.generated.js');
const smallWords = new Set([
  "og", "i", "på", "til", "af", "med", "for", "and", "the", "of", "in", "on", "at", "to", "with", "ved", "om"
]);

// --- Helper Functions ---

/**
 * Sorts file/directory names alpha-numerically (e.g., "01_..." < "02_..." < "10_...").
 */
const naturalSort = (a, b) => a.name.localeCompare(b.name, undefined, { numeric: true, sensitivity: 'base' });

/**
 * Creates a "beautified" human-readable name from a file or directory name.
 */
function beautifyName(name) {
  let clean = name.replace(/\.md$/i, ''); // Remove .md extension
  
  // --- SỬA LỖI TẠI ĐÂY ---
  // ĐÃ XÓA DÒNG SAU ĐỂ GIỮ LẠI CÁC CHỮ SỐ Ở ĐẦU:
  // clean = clean.replace(/^\d+[\s.-_]/, ''); 
  // --- KẾT THÚC SỬA LỖI ---

  clean = clean.replace(/[-_]/g, ' '); // Replace hyphens/underscores with spaces
  clean = clean.replace(/\s+/g, ' ').trim(); // Normalize whitespace

  // Apply Title Case (lowercasing small words unless they are the first word)
  return clean.split(' ').map((word, index) => {
    const lowerWord = word.toLowerCase();
    if (index === 0 || !smallWords.has(lowerWord)) {
      // Xử lý trường hợp từ rỗng (nếu clean.trim() là "")
      if (word.length === 0) return ""; 
      return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
    }
    return lowerWord;
  }).join(' ');
}

/**
 * Generates the application-specific hash (relative path) for a link's href.
 */
function makeHashFromRelativeMd(relativePath) {
  const withoutExt = relativePath.replace(/\.md$/i, '');
  const hash = withoutExt.replace(/\\/g, '/'); // Chuẩn hóa Windows path
  return `#${hash}`;
}

/**
 * =============================================================================
 * NEW RECURSIVE FUNCTION (v3 - "True Tree")
 * =============================================================================
 */
function buildRecursiveTree(dirPath) {
  const children = [];
  let entries;
  try {
    entries = fs.readdirSync(dirPath, { withFileTypes: true })
      .filter(e => !e.name.startsWith('.')) 
      .sort(naturalSort);
  } catch (e) {
    console.error(`[gen-nav] ❌ Lỗi khi đọc thư mục: ${dirPath}`, e);
    return [];
  }

  for (const entry of entries) {
    const entryPath = path.join(dirPath, entry.name);
    
    if (entry.isDirectory()) {
      const nestedChildren = buildRecursiveTree(entryPath);
      if (nestedChildren.length > 0) {
        children.push({
          type: 'category',
          title: beautifyName(entry.name),
          children: nestedChildren,
        });
      }
    } else if (entry.isFile() && entry.name.endsWith('.md')) {
      const relPath = path.relative(markdownDir, entryPath);
      children.push({
        type: 'link',
        text: beautifyName(entry.name),
        href: makeHashFromRelativeMd(relPath),
      });
    }
  }
  return children;
}

/**
 * Main function to generate the navigation data file.
 */
function runGeneration() {
  const modules = [];
  if (!fs.existsSync(markdownDir)) {
    console.error(`[gen-nav] Error: Directory not found: ${markdownDir}`);
    return;
  }

  const moduleDirs = fs.readdirSync(markdownDir, { withFileTypes: true })
    .filter(e => e.isDirectory() && !e.name.startsWith('.'))
    .sort(naturalSort);

  for (const moduleDir of moduleDirs) {
    const modulePath = path.join(markdownDir, moduleDir.name);
    const children = buildRecursiveTree(modulePath); 
    
    if (children.length > 0) {
      modules.push({
        title: beautifyName(moduleDir.name),
        categories: children, 
      });
    }
  }

  // --- Ghi file ---
  try {
    const fileContent = `// This file is auto-generated by scripts/generate-navigation.js
// Do not edit this file manually!
// Generated at: ${new Date().toISOString()}

export const restructuredNavData = ${JSON.stringify(modules, null, 2)};
`;
    fs.writeFileSync(outputFile, fileContent, 'utf-8');
    console.log(`[gen-nav] ✅ Wrote ${modules.length} modules to ${path.relative(path.join(__dirname, '..'), outputFile)}`);
  } catch (error) {
    console.error(`[gen-nav] ❌ Failed to generate navigation:`, error);
  }
}

// --- Main Execution ---

runGeneration();

if (process.argv.includes('--watch')) {
  console.log(`[gen-nav] 👁️  Watching for changes in: ${markdownDir}`);
  const watcher = chokidar.watch(markdownDir, {
    ignored: /(^|[\/\\])\../,
    persistent: true,
    ignoreInitial: true,
  });

  const handleFileChange = (event, p) => {
    console.log(`[gen-nav] 🔄 Change detected (${event}): ${path.basename(p)}. Regenerating...`);
    runGeneration();
  };

  watcher
    .on('add', (p) => handleFileChange('add', p))
    .on('unlink', (p) => handleFileChange('unlink', p))
    .on('addDir', (p) => handleFileChange('addDir', p))
    .on('unlinkDir', (p) => handleFileChange('unlinkDir', p))
    .on('error', (error) => console.error(`[gen-nav] ❌ Watcher error: ${error}`));
}